using Godot;
using System;

public partial class weapons : players_glob
{

    public char[] Weapons {get; set;}
    public int Pos;


    public void WeaponsSet(char[] w){

        for(int i = 0; i < w.Length; i++){
            Weapons[i] = w[i];
        }

    }

    public void NextW(){

        Pos++;

        Propertys();

    }

    public void PreviousW(){

        Pos--;

        Propertys();

    }

    public void ResetW(){

        Pos = 0;

        Propertys();


    }

    public void Propertys(){
    
        if (Weapons[Pos] == 'S'){
            Damage = 50;
        }

        if(Weapons[Pos] == 'A'){
            Damage = 80;
        }

        if(Weapons[Pos] == 'P'){
            Damage = 10;
        }

        if(Weapons[Pos] == 'N'){
            Damage = 0;
        }
        
    }

}

